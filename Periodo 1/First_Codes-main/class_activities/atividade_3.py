lista = [1,4,2,3,4,2,3,4]

def repeated(lista):
  rep = []
  for i in range(len(lista)):
    if lista.index(lista[i]) != i:
      if lista[i] not in rep:
        rep.append(lista[i])
  return sorted(rep)

repeated(lista)