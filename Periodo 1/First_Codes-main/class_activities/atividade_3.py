x = float(input("Valor de X: "))
y = float(input("Valor de Y: "))

def fun(x,y):
  if x >= 0 and y >= 0:
    z = x + y
  elif x >= 0 and y < 0:
    z = x + y**2
  elif x < 0 and y >= 0:
    z = x**2 + y
  elif x < 0 and y < 0:
    z = x**2 + x**2
  return z

fun(x,y)

