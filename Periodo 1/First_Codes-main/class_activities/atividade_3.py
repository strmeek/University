import numpy as np

pos = int(input("Quantidade de posições do vetor: "))
x = []

for k in range(pos):
  x.append(float(input("Valor da Posição %d: " %k)))

def vetor(x):
  var = np.var(x)
  dp = np.std(x)
  print("Variância: %.2f Desvio Padrão: %.2f" %(var,dp))

